package com.capg.jobportal.enums;

/*
 * ================================================================
 * AUTHOR: Billa Abijith Chowdary
 * ENUM: JobType
 * DESCRIPTION:
 * This enum defines different types of job employment categories
 * supported by the platform.
 *
 * POSSIBLE VALUES:
 * - FULL_TIME → Full-time employment
 * - PART_TIME → Part-time employment
 * - REMOTE    → Work-from-home / remote job
 * - CONTRACT  → Contract-based job
 *
 * PURPOSE:
 * Provides a standardized way to represent job types and ensures
 * data consistency across the system.
 * ================================================================
 */
public enum JobType {
	FULL_TIME, 
	PART_TIME , 
	REMOTE , 
	CONTRACT
}	
